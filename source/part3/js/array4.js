var numArray = [1,2,3,4,5];

console.log('Array:', numArray);
console.log('concat(6,7): ', numArray.concat(6,7));
console.log('slice(0,2):', numArray.slice(0,2));
console.log('slice(3):', numArray.slice(3));
console.log('slice(0,-3):', numArray.slice(0, -3));
console.log('splice(4):', numArray.splice(4));
console.log('splice(1,2):', numArray.splice(1, 2));

