var count = 0;

console.log('Example: for')
for(count=0; count < 5; count++){
  console.log('count:', count);

}

count = 0;
console.log('Example: while')

while(count < 5){
  console.log('count:', count);
  count++;
}

count = 0;
console.log('Example: do-while')

do{
  console.log('count:', count);
  count++;
}while(count < 5);
